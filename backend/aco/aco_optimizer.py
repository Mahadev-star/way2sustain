@dataclass
class RealTimeFactors:
    """Real-time environmental factors for route calculation"""
    traffic_congestion: float = 0.3      # 0-1 scale
    air_quality_index: float = 50.0     # 0-500 AQI
    weather_impact: float = 0.1         # 0-1 scale
    elevation_gain: float = 0.0          # meters
    temperature: float = 25.0           # celsius
    
    def to_dict(self) -> Dict:
        return {
            'traffic_congestion': self.traffic_congestion,
            'air_quality_index': self.air_quality_index,
            'weather_impact': self.weather_impact,
            'elevation_gain': self.elevation_gain,
            'temperature': self.temperature,
        }
    
    @staticmethod
    def generate(seed: int = None) -> 'RealTimeFactors':
        """Generate real-time factors with variation"""
        if seed is not None:
            random.seed(seed)
        
        return RealTimeFactors(
            traffic_congestion=random.uniform(0.2, 0.7),
            air_quality_index=random.uniform(30, 120),
            weather_impact=random.uniform(0.0, 0.4),
            elevation_gain=random.uniform(0, 100),
            temperature=random.uniform(20, 35),
        )


@dataclass
class Waypoint:
    """A waypoint generated by ACO"""
    lat: float
    lon: float
    order: int
    # Environmental values at this waypoint
    elevation: float = 0.0
    road_type: str = 'road'
    estimated_traffic: float = 0.3
    distance_from_prev: float = 0.0
    cumulative_distance: float = 0.0
    
    def to_dict(self) -> Dict:
        return {
            'lat': self.lat,
            'lon': self.lon,
            'order': self.order,
            'elevation': self.elevation,
            'road_type': self.road_type,
            'estimated_traffic': self.estimated_traffic,
            'distance_from_prev': self.distance_from_prev,
            'cumulative_distance': self.cumulative_distance,
        }


@dataclass
class Ant:
    """Represents an ant in the ACO algorithm"""
    id: str
    route_type: str = 'balanced'
    waypoints: List[Waypoint] = field(default_factory=list)
    visited: set = field(default_factory=set)
    fitness: float = float('inf')
    
    # Route metrics
    total_distance_km: float = 0.0
    total_time_minutes: float = 0.0
    total_co2_kg: float = 0.0
    eco_points: int = 0
    
    # Pheromone level
    pheromone: float = 1.0
    
    def reset(self):
        self.waypoints = []
        self.visited = set()
        self.fitness = float('inf')
        self.total_distance_km = 0.0
        self.total_time_minutes = 0.0
        self.total_co2_kg = 0.0
        self.eco_points = 0
        self.pheromone = 1.0


