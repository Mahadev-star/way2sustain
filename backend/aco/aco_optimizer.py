class ACOOptimizer:
    """
    Main ACO Optimizer - Combines ACO waypoint generation with OSRM route drawing
    
    Flow:
    1. ACO generates waypoints using environmental factors
    2. Waypoints are passed to OSRM
    3. OSRM draws real routes through waypoints
    4. Environmental values calculated for final route
    """
    
    def __init__(self):
        self.aco_core = ACOCore()
        self.osrm = OSRMConnector()
    
    def set_real_time_factors(self, factors: RealTimeFactors):
        """Set real-time environmental factors"""
        self.aco_core.set_real_time_factors(factors)
    
    def optimize_route(
        self, 
        start: Tuple[float, float], 
        end: Tuple[float, float],
        route_type: str = 'eco',
        vehicle_type: str = 'electric car'
    ) -> Dict:
        """
        Optimize route using ACO + OSRM
        
        Returns:
            Dictionary with waypoints, route details, environmental values, eco points
        """
        print(f"\n{'='*60}")
        print(f"ACO OPTIMIZATION: {route_type.upper()}")
        print(f"From: {start} To: {end}")
        print(f"{'='*60}")
        
        # Step 1: Generate waypoints using ACO
        print("Step 1: Generating waypoints with ACO...")
        ant = self.aco_core.run_aco(start, end, route_type)
        
        if ant is None or not ant.waypoints:
            print("ACO failed to generate waypoints")
            return self._fallback_result(start, end, route_type, vehicle_type)
        
        print(f"  Generated {len(ant.waypoints)} waypoints")
        
        # Step 2: Draw real route through waypoints using OSRM
        print("Step 2: Drawing real route with OSRM...")
        osrm_route = self.osrm.draw_route_through_waypoints(ant.waypoints)
        
        if osrm_route:
            route_details = self.osrm.extract_route_details(osrm_route)
            actual_distance = route_details.get('distance_km', ant.total_distance_km)
            actual_duration = route_details.get('duration_min', ant.total_time_minutes)
            actual_coordinates = route_details.get('coordinates', [])
        else:
            # Fallback: use waypoint coordinates
            actual_distance = ant.total_distance_km
            actual_duration = ant.total_time_minutes
            actual_coordinates = [(wp.lon, wp.lat) for wp in ant.waypoints]
        
        # Step 3: Calculate environmental values
        print("Step 3: Calculating environmental values...")
        
        # Calculate CO2 emissions
        base_emission = BASE_EMISSIONS.get(vehicle_type.lower(), 120.0)
        co2_emissions = (base_emission * actual_distance) / 1000
        baseline_co2 = 120.0 * actual_distance / 1000
        
        # Calculate eco points
        eco_result = calc_eco_points(
            co2_emissions=co2_emissions,
            baseline_co2=baseline_co2,
            average_aqi=self.aco_core.real_time_factors.air_quality_index,
            traffic_level=self.aco_core.real_time_factors.traffic_congestion,
            time_vs_baseline=0,
            vehicle_type=vehicle_type,
        )
        
        # Build result with Flutter-compatible field names
        result = {
            # Flutter-compatible fields
            'type': route_type.upper(),
            'description': 'Most environmentally friendly route' if route_type == 'eco' else ('Best compromise between time and environment' if route_type == 'balanced' else 'Fastest route - most direct path'),
            'icon': '🌿' if route_type == 'eco' else ('⚖️' if route_type == 'balanced' else '⏱️'),
            
            # Points for map - convert from [lon, lat] to [lat, lon]
            'points': [[coord[1], coord[0]] for coord in actual_coordinates] if actual_coordinates else [{'lat': start[0], 'lon': start[1]}, {'lat': end[0], 'lon': end[1]}],
            
            # Route details - Flutter expects 'distance' and 'duration'
            'distance': round(actual_distance, 2),
            'duration': round(actual_duration, 1),
            
            # Environmental values
            'co2_emissions': round(co2_emissions, 3),
            'co2_savings': round(eco_result.get('co2_savings', 0), 1),
            'eco_points': eco_result['eco_points'],
            
            # Additional fields
            'route_type': route_type,
            'vehicle_type': vehicle_type,
            'averageAQI': self.aco_core.real_time_factors.air_quality_index,
            'trafficLevel': self.aco_core.real_time_factors.traffic_congestion,
            
            # Real-time factors
            'real_time_factors': self.aco_core.real_time_factors.to_dict(),
            
            # Time vs baseline (placeholder)
            'time_vs_baseline': 0,
            
            # ACO metrics
            'aco_fitness': ant.fitness,
            'waypoint_count': len(ant.waypoints),
        }
        
        print(f"  Distance: {result['distance']} km")
        print(f"  Duration: {result['duration']} min")
        print(f"  CO2: {result['co2_emissions']} kg")
        print(f"  Eco Points: {result['eco_points']}")
        
        return result
    
    def _fallback_result(
        self, 
        start: Tuple[float, float], 
        end: Tuple[float, float],
        route_type: str,
        vehicle_type: str
    ) -> Dict:
        """Fallback when ACO fails"""
        # Simple direct route
        distance = self.aco_core._haversine_distance(start, end)
        duration = (distance / 40) * 60
        
        base_emission = BASE_EMISSIONS.get(vehicle_type.lower(), 120.0)
        co2 = (base_emission * distance) / 1000
        
        # Fallback result with Flutter-compatible fields
        return {
            'type': route_type.upper(),
            'description': 'Most environmentally friendly route' if route_type == 'eco' else ('Best compromise between time and environment' if route_type == 'balanced' else 'Fastest route'),
            'icon': '🌿' if route_type == 'eco' else ('⚖️' if route_type == 'balanced' else '⏱️'),
            'points': [{'lat': start[0], 'lon': start[1]}, {'lat': end[0], 'lon': end[1]}],
            'distance': round(distance, 2),
            'duration': round(duration, 1),
            'co2_emissions': round(co2, 3),
            'co2_savings': 0,
            'eco_points': 50,
            'route_type': route_type,
            'vehicle_type': vehicle_type,
            'averageAQI': 50.0,
            'trafficLevel': 0.3,
            'time_vs_baseline': 0,
            'real_time_factors': self.aco_core.real_time_factors.to_dict(),
            'aco_fitness': float('inf'),
            'waypoint_count': 2,
        }
    
    def find_all_routes(
        self, 
        start: Tuple[float, float], 
        end: Tuple[float, float],
        vehicle_type: str = 'electric car'
    ) -> Dict[str, Dict]:
        """
        Find all three route types (eco, balanced, quickest)
        
        Returns:
            Dictionary with 'eco', 'balanced', 'quickest' keys
        """
        print(f"\n{'='*60}")
        print("FINDING ALL ROUTES WITH ACO + OSRM")
        print(f"Start: {start}, End: {end}")
        print(f"Vehicle: {vehicle_type}")
        print(f"{'='*60}")
        
        results = {}
        
        # Eco route
        self.aco_core.real_time_factors = RealTimeFactors.generate(seed=1)
        self.aco_core.real_time_factors.traffic_congestion = 0.3
        results['eco'] = self.optimize_route(start, end, 'eco', vehicle_type)
        
        # Balanced route
        self.aco_core.real_time_factors = RealTimeFactors.generate(seed=2)
        results['balanced'] = self.optimize_route(start, end, 'balanced', vehicle_type)
        
        # Quickest route
        self.aco_core.real_time_factors = RealTimeFactors.generate(seed=3)
        self.aco_core.real_time_factors.traffic_congestion = 0.5
        results['quickest'] = self._get_direct_osrm_route(start, end, vehicle_type)
        
        return results

    def _get_direct_osrm_route(self, start, end, vehicle_type):
        """Get direct route from OSRM without ACO - for quickest route"""
        import requests
        coord = f"{start[1]},{start[0]};{end[1]},{end[0]}"
        url = f"{self.osrm.base_url}/route/v1/driving/{coord}"
        try:
            resp = requests.get(url, params={'overview': 'full', 'geometries': 'geojson'}, timeout=15)
            if resp.status_code == 200:
                data = resp.json()
                if data.get('code') == 'Ok' and data.get('routes'):
                    route = data['routes'][0]
                    details = self.osrm.extract_route_details(route)
                    dist = details.get('distance_km', 0)
                    dur = details.get('duration_min', 0)
                    coords = details.get('coordinates', [])
                    base_em = BASE_EMISSIONS.get(vehicle_type.lower(), 120.0)
                    co2 = (base_em * dist) / 1000
                    return {
                        'type': 'QUICKEST', 'description': 'Fastest route - most direct path',
                        'icon': '⏱️', 'points': [[c[1],c[0]] for c in coords] if coords else [{'lat':start[0],'lon':start[1]},{'lat':end[0],'lon':end[1]}],
                        'distance': round(dist,2), 'duration': round(dur,1), 'co2_emissions': round(co2,3),
                        'co2_savings': 0, 'eco_points': 0, 'route_type': 'quickest', 'vehicle_type': vehicle_type,
                        'averageAQI': 50.0, 'trafficLevel': 0.5, 'time_vs_baseline': 0,
                        'real_time_factors': RealTimeFactors().to_dict(), 'aco_fitness': 0.0, 'waypoint_count': 2,
                    }
        except Exception as e:
            print(f"Direct OSRM error: {e}")
        return self._fallback_result(start, end, 'quickest', vehicle_type)


