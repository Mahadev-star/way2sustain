def handle_aco_route_request(data: Dict) -> Dict:
    """
    Handle ACO route request from API
    
    Expected input:
    {
        "start": [lat, lon],
        "end": [lat, lon],
        "vehicle_type": "electric car"
    }
    """
    start = tuple(data.get('start', [0, 0]))
    end = tuple(data.get('end', [0, 0]))
    vehicle_type = data.get('vehicle_type', 'electric car')
    
    # Validate coordinates
    if start == (0, 0) or end == (0, 0):
        return {'error': 'Invalid coordinates', 'routes': {}}
    
    # Initialize optimizer
    optimizer = ACOOptimizer()
    
    # Find all routes
    routes = optimizer.find_all_routes(start, end, vehicle_type)
    
    return {
        'success': True,
        'start': list(start),
        'end': list(end),
        'vehicle_type': vehicle_type,
        'routes': routes,
        'timestamp': datetime.now().isoformat(),
    }


