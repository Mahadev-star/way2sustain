class OSRMConnector:
    """Handles OSRM route drawing using ACO waypoints"""
    
    def __init__(self, base_url: str = "http://router.project-osrm.org"):
        self.base_url = base_url
    
    def draw_route_through_waypoints(
        self, 
        waypoints: List[Waypoint],
        route_type: str = 'driving'
    ) -> Optional[Dict]:
        """Use OSRM to draw real route through waypoints"""
        import requests
        
        if not waypoints or len(waypoints) < 2:
            return None
        
        # Build coordinate string for OSRM
        # Format: lon,lat;lon,lat;...
        coord_string = ";".join([f"{wp.lon},{wp.lat}" for wp in waypoints])
        
        url = f"{self.base_url}/route/v1/{route_type}/{coord_string}"
        params = {
            'overview': 'full',
            'geometries': 'geojson',
            'steps': 'true',
            'annotations': 'true',
        }
        
        try:
            response = requests.get(url, params=params, timeout=15)
            if response.status_code == 200:
                data = response.json()
                if data.get('code') == 'Ok' and data.get('routes'):
                    return data['routes'][0]
        except Exception as e:
            print(f"OSRM error: {e}")
        
        return None
    
    def extract_route_details(self, osrm_route: Dict) -> Dict:
        """Extract route details from OSRM response"""
        if not osrm_route:
            return {}
        
        # Get geometry
        geometry = osrm_route.get('geometry', {})
        coordinates = geometry.get('coordinates', [])
        
        # Get distance and duration
        distance = osrm_route.get('distance', 0)  # meters
        duration = osrm_route.get('duration', 0)  # seconds
        
        # Get legs
        legs = osrm_route.get('legs', [])
        
        return {
            'coordinates': coordinates,  # [lon, lat] format
            'distance_km': distance / 1000,
            'duration_min': duration / 60,
            'legs': legs,
        }


